const AddressSelect = ({ control, name, label, required, error, placeholder }) => {
    return (
        <Controller
            render={({ field: { onChange, value } }) => (
                <Autocomplete
                    onChange={(e, item) => {
                        onChange(item)
                    }}
                    value={value}
                    options={addressList}
                    getOptionLabel={item => item ? item : ''}
                    isOptionEqualToValue={(option, value) => option === value}
                    freeSolo
                    renderInput={params => (
                        <TextField
                            {...params}
                            label={placeholder}
                            variant="outlined"
                            placeholder={placeholder}
                            required={required}
                            error={error}
                            InputLabelProps={{
                                shrink: true,
                            }}
                        />
                    )}
                />
            )
            }
            name={name}
            id={name}
            control={control}
        />
    );
}

